public class MathUtil {
    public static int somar(int n1, int n2){
        return n1 + n2;
    }

    public static double calcAreaQuadrado(double lado){
        return lado * lado;
    }

    public static double calcAreaCirculo(double raio){
        double area = 3.14 * raio * raio;
        return area;
    }
}
